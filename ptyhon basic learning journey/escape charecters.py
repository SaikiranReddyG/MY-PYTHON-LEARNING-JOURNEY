#escape charecters
#In String literals we can use esacpe characters to associate a special meaning[
#\n         #used to create new line
s = "durga \n nsoftware \n sai \n kiran \n reddy"
print(s)
#\t         #used to create a horizontal tab
s1 = "durga \t nsoftware \t sai \t kiran \t reddy"
print(s1)
#\r        #used to create carriage return
s2 = "durga \r nsoftware \r sai \r kiran \r reddy"
print(s2)
#\b         #used to create back space
s3 = "durga \b nsoftware \b sai \b kiran \b reddy"
print(s3)
#\f         #used to create form feed
s4 = "durga \f nsoftware \f sai \f kiran \f reddy"
print(s4)

#\v         #used to create vertical tab
s5 = "durga \v nsoftware \v sai \v kiran \v reddy"
print(s5)
#\'           #used to create single quote
s6 = "durga \' nsoftware \' sai \' kiran \' reddy"
#\"           #used to create double quote
s7 = "durga \" nsoftware \" sai \" kiran \" reddy"
#\\           #used to create back slash
s8 = "durga \\ nsoftware \\ sai \\ kiran \\ reddy"
print(s6)
print(s7)
print(s8)