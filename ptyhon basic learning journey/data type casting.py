#conversion of one type value to another type value is called DATA TYPE CASTING

#int()
#we can convert any type of value to int type except complex numbers
a = 55.55
print(int(a))
f = 123.45
print(int(f))
b = 0B1111
print(int(b))

#float()
#we can convert any type of value to float type except complex numbers
a = 55
print(float(a))

#complex()
#we can convert any type to complex numbers
U = 2.77
print(complex(U))

#BOOLEAN
a = 3
print(bool(a))
print(bool(''))

#string()
s = 3333
print(str(s))
d = 4343.45
print(str(d))
c = True
print(str(c))